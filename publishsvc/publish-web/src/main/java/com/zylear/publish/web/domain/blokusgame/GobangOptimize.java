    private Long id;

    private String allChess;

    private Integer score;

    private Integer x;

    private Integer y;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getAllChess() {
        return allChess;
    }

    public void setAllChess(String allChess) {
        this.allChess = allChess;
    }

